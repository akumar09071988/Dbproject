package utdallas.librarymanagement.bean;

public class FineList {
    
    private String loanId;
    private String cardNo;
    private String fname;
    private String lname;
    private String bookId;
    private String branchId;
    private Double fineAmountToBePaid;
    
	/**
	 * @return the fname
	 */
	public String getFname() {
		return fname;
	}
	/**
	 * @param fname the fname to set
	 */
	public void setFname(String fname) {
		this.fname = fname;
	}
	/**
	 * @return the lname
	 */
	public String getLname() {
		return lname;
	}
	/**
	 * @param lname the lname to set
	 */
	public void setLname(String lname) {
		this.lname = lname;
	}
	/**
	 * @return the bookId
	 */
	public String getBookId() {
		return bookId;
	}
	/**
	 * @param bookId the bookId to set
	 */
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	/**
	 * @return the loanId
	 */
	public String getLoanId() {
		return loanId;
	}
	/**
	 * @param loanId the loanId to set
	 */
	public void setLoanId(String loanId) {
		this.loanId = loanId;
	}
	/**
	 * @return the cardNo
	 */
	public String getCardNo() {
		return cardNo;
	}
	/**
	 * @param cardNo the cardNo to set
	 */
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	/**
	 * @return the branchId
	 */
	public String getBranchId() {
		return branchId;
	}
	/**
	 * @param branchId the branchId to set
	 */
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}
	/**
	 * @return the fineAmountToBePaid
	 */
	public Double getFineAmountToBePaid() {
		return fineAmountToBePaid;
	}
	/**
	 * @param fineAmountToBePaid the fineAmountToBePaid to set
	 */
	public void setFineAmountToBePaid(Double fineAmountToBePaid) {
		this.fineAmountToBePaid = fineAmountToBePaid;
	}

    

}
